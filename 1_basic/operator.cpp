# include<iostream>
using namespace std;
int main(){
   int num1=6;
   int num2=3;
   // Arithmatic operation
   cout<<num1+num2<<endl;
   cout<<num1-num2<<endl;
   cout<<num1*num2<<endl;
   cout<<num1/num2<<endl;
   cout<<num1%num2<<endl;
  // relation operation
   cout<<(num1==num2)<<endl;
   cout<<(num1!=num2)<<endl;
   cout<<(num1>=num2)<<endl;
   cout<<(num1<=num2)<<endl;

   return 0;
}
